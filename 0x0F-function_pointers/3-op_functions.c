#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - Functions that returns the sum of 2 int
 * @a : first integer
 * @b : second integer
 *
 * Return: Always (0) success
 */
int op_add(int a, int b)
{

	return (a + b);
}
/**
 * op_sub - Function gives the difference of 2 integers
 * @a : first integer
 * @b : second integer
 *
 * Return: Always (0) success
 */
int op_sub(int a, int b)
{

	return (a - b);
}
/**
 * op_mul -  Function gives the product of 2 integers
 * @a : first integer
 * @b : second integer
 *
 * Return: Always (0) success
 */
int op_mul(int a, int b)
{

	return (a * b);
}
/**
 * op_div - function gives the division of 2 integers
 * @a : first integer
 * @b : second integer
 *
 * Return: Always (0) success
 */
int op_div(int a, int b)
{

	return (a / b);
}
/**
 * op_mod - Function gives modulus
 * @a : first integer
 * @b : second integer
 *
 * Return: Always (0) success
 */
int op_mod(int a, int b)
{

	return (a % b);
}
